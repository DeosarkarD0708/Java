public class forLoop9 {
	
	public static void main(String[]args) {
		
		//WAP to calculate sum of first 15 numbers using for loop
		
		int sum = 0;
		
		for(int i=1; i<=15;i++) {
			
			sum =  sum + i;
		}
			System.out.println("Addition of first 15 numbers :"+sum);
		}
	}
		

