package LoopStatement;

public class forLoop10 {
	
	public static void main(String[]args) {
		
		//WAP of multiplication table of 2 using for loop
		

		int num = 2; 
		
		for(int i= 1; i<=10; i++) {
			
			System.out.println("Multiplication table :" +i*num);
			
		}
		
		
	}

}
