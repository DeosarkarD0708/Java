package LoopStatement;

public class whileLoop7 {

	public static void main(String []args) {
		
		//WAP addition of first 15 numbers using while loop
		
		int i=1;
		int sum = 0;
		while(i<=15) {
			 sum = sum+i;
	
			System.out.println("Addition of first 15 numbers:"+sum);
		   i++;
		  
		}
	}
}
