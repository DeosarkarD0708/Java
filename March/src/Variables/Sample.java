package Variables;

public class Sample {
	
	public static void main(String[]args){ 
		 
		//1.Variable Declaration
		
		String StudentFirstName;
		String StudentLastName;
		int RollNo;
		char StudentGrade;
		float StudentPercentage;
		
		//2.Variable Initialization (Assigning)
		
		StudentFirstName = "Ojas";
		StudentLastName = "Deosarkar";
		RollNo = 10;
		StudentGrade = 'A';
		StudentPercentage = 74.76f;
		 
		//Usages
		
		System.out.println("FirstName :" +StudentFirstName);
		System.out.println("LastName :" +StudentLastName);
		System.out.println("RollNo :"+RollNo);
		System.out.println("StudentGrade :" +StudentGrade);
		System.out.println("StudentPercentage :" +StudentPercentage);
		
		
	}
}
